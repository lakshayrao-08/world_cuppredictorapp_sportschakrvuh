function calculateThirdPlaceAdvancement() {
    const fallbackTexts = {
        "Winner_A": "3rd Group A/B/C/D/F",
        "Winner_B": "3rd Group C/D/F/G/H",
        "Winner_C": "3rd Group C/E/F/H/I",
        "Winner_D": "3rd Group E/H/I/J/K",
        "Winner_E": "3rd Group A/E/H/I/J",
        "Winner_F": "3rd Group B/E/F/I/J",
        "Winner_G": "3rd Group E/F/G/I/J",
        "Winner_H": "3rd Group D/E/I/J/L",


    };

    if (selectedCount !== 8) {
        Object.keys(bracketTargets).forEach(key => {
            if (bracketTargets[key]) {
                bracketTargets[key].innerHTML = fallbackTexts[key]; // innerHTML lagaya
            }
        });
        return; 
    }

    let activeGroups = [];
    let teamNamesMap = {}; 

    freshBtns.forEach(btn => {
        if (btn && btn.classList.contains('active')) {
            const groupLetter = buttonGroupMap.get(btn);
            if (groupLetter) {
                activeGroups.push(groupLetter);
                teamNamesMap[groupLetter] = btn.innerText.trim(); 
            }
        }
    });

    const lookupKey = activeGroups.sort().join('');
    const assignments = FIFA_3RD_PLACE_MATRIX[lookupKey];

    if (!assignments) {
        console.warn("Matrix key missing:", lookupKey);
        return;
    }

    const winnersOrder = ["Winner_A", "Winner_B", "Winner_C", "Winner_D", "Winner_E", "Winner_F", "Winner_G", "Winner_H"];

    winnersOrder.forEach((winnerKey, index) => {
        const sourceGroup = assignments[index]; 
        const targetElement = bracketTargets[winnerKey]; 
        
        if (targetElement) {
            // Forcefully innerHTML change kar rahe hain bina default standard error ke
            targetElement.innerHTML = teamNamesMap[sourceGroup] || `3rd Group ${sourceGroup}`;
        }
    });
}


function updateButtonLimits() {
    freshBtns.forEach(btn => {
        if (btn) {
            if (selectedCount === 8 && !btn.classList.contains('active')) {
                btn.style.pointerEvents = 'none';
                btn.style.opacity = '0.4';
            } else {
                btn.style.pointerEvents = 'auto';
                btn.style.opacity = '1';
            }
        }
    });
}

function handleButtonClick(element) {
    if (!element) return;

    if (element.classList.contains('active')) {
        element.classList.remove('active');
        selectedCount--;
    } else if (selectedCount < 8) {
        element.classList.add('active');
        selectedCount++;
        if (typeof resultDisplay !== 'undefined' && resultDisplay) {
            resultDisplay.innerText = "The winner is: " + element.innerText.trim();
        }
    }
    updateButtonLimits();
    calculateThirdPlaceAdvancement(); 
}


freshBtns.forEach(btn => {
    btn.addEventListener('click', () => handleButtonClick(btn));
});

